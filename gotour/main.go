package main

import (
	"fmt"
)

func main() {
	i, j := 42, 2701

	p := &i         // p は i を指す
	fmt.Println(*p) // ポインタを通してiを読み取る
	*p = 21         // ポインタを通じてiを設定する
	fmt.Println(i)  // iの新しい値を見る

	p = &j         // p は j を指す
	*p = *p / 37   // ポインタを通じてjを割る
	fmt.Println(j) // jの新しい値を見る
}
